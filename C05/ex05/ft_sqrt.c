int	ft_sqrt(int nb)
{
	int	i;
	int	e;

	i = 1;
	e = 0;
	while (i <= nb)
	{
		e = i * i;
		if (e == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
